import UIKit

extension UIColor {
    public class var sapiensBorderColor: UIColor! {
        get {
            return UIColor(red: 38.0/255.0, green: 47.0/255.0, blue: 73.0/255.0, alpha: 1.0)
        }
    }
    
    public class var sapiensPinkColor: UIColor! {
        get {
            return UIColor(red: 251.0/255.0, green: 93.0/255.0, blue: 158.0/255.0, alpha: 1.0)
        }
    }
    
    public class var sapiensPurpleColor: UIColor! {
        get {
            return UIColor(red: 148.0/255.0, green: 127.0/255.0, blue: 238.0/255.0, alpha: 1.0)
        }
    }
}
